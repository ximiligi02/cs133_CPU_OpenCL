unsigned int __binary_curry_len = 2174;


// Sequential CNN implementation
void conv(float Cout[NUM][OUTIMROW][OUTIMROW], float Cin[NUM][INIMROW][INIMROW],
          float weight[NUM][NUM][KERNEL][KERNEL], float bias[NUM]) {
    static float C[NUM][IMROW][IMROW];

    for (int i = 0; i < NUM; i++) {
        for (int h = 0; h < IMROW; h++) {
            for (int w = 0; w < IMROW; w++)
                C[i][h][w] = bias[i];
        }
    }

    /* Initialize OpenCL environment */

    // CL return status code.
    cl_int status;

    // The number of platforms
    cl_uint numPlatforms = 0;
    status = clGetPlatformIDs(0, NULL, &numPlatforms);
    
    // Allocate platform id structures.
    cl_platform_id *platforms = NULL;
    platforms = (cl_platform_id *) malloc(numPlatforms * sizeof(cl_platform_id));
    status = clGetPlatformIDs(numPlatforms, platforms, NULL);

    // Find CPU
    int platform_index = -1;
    for (int i = 0; i < numPlatforms; i++) {
        char vendor[128];
        status = clGetPlatformInfo(platforms[i], CL_PLATFORM_VENDOR, sizeof(vendor), vendor, NULL);
        vendor[6] = '\0';
        if (strcmp(vendor, "Intel(") == 0) {
            platform_index = i;
            break;
        }
    }

    if (platform_index == -1) {
        printf("CPU platform not found!\n");
        exit(1);
    }

    // Number of devices
    cl_uint numDevices = 0;
    status = clGetDeviceIDs(platforms[platform_index], CL_DEVICE_TYPE_CPU, 0, NULL, &numDevices);

    // Get all ids
    cl_device_id *devices;
    devices = (cl_device_id *) malloc(numDevices * sizeof(cl_device_id));

    // Fill the devices
    status = clGetDeviceIDs(platforms[platform_index], CL_DEVICE_TYPE_CPU, numDevices, devices, NULL);

    cl_context context; 
    fprintf(stderr, "%d devices\n", numDevices);
    context = clCreateContext(NULL, numDevices, devices, NULL, NULL, &status);

    // Create a command queue and associate it with the device
    cl_command_queue cmdQueue;
    cmdQueue = clCreateCommandQueue(context, devices[0], 0, &status);
    
    if (status != CL_SUCCESS) {
        fprintf(stderr, "%s\n", "command queue");
        exit(1);
    }


    cl_mem conv_layer_ibuf;
    conv_layer_ibuf = clCreateBuffer(context, CL_MEM_READ_ONLY, INIMROW * INIMROW * NUM * sizeof(float), NULL, &status);

    if (status != CL_SUCCESS) {
        fprintf(stderr, "%s\n", "conv_layer_ibuf");
        exit(1);
    }

    cl_mem conv_layer_wbuf;
    conv_layer_wbuf = clCreateBuffer(context, CL_MEM_READ_ONLY, KERNEL * KERNEL * NUM * NUM * sizeof(float), NULL, &status);

    if (status != CL_SUCCESS) {
        fprintf(stderr, "%s\n", "conv_layer_wbuf");
        exit(1);
    }

    cl_mem conv_layer_obuf;
    conv_layer_obuf = clCreateBuffer(context, CL_MEM_READ_WRITE, IMROW * IMROW * NUM * sizeof(float), NULL, &status);

    if (status != CL_SUCCESS) {
        fprintf(stderr, "%s\n", "conv_layer_obuf");
        exit(1);
    }

    status = clEnqueueWriteBuffer(cmdQueue, conv_layer_ibuf, CL_FALSE, 0, INIMROW * INIMROW * NUM * sizeof(float), Cin, 0, NULL, NULL);

    if (status != CL_SUCCESS) {
        fprintf(stderr, "%s\n", "ewb ibuf");
        exit(1);
    }

    status = clEnqueueWriteBuffer(cmdQueue, conv_layer_wbuf, CL_FALSE, 0, KERNEL * KERNEL * NUM * NUM * sizeof(float), weight, 0, NULL, NULL);

    if (status != CL_SUCCESS) {
        fprintf(stderr, "%s\n", "ewb wbuf");
        exit(1);
    }

    status = clEnqueueWriteBuffer(cmdQueue, conv_layer_obuf, CL_FALSE, 0, IMROW * IMROW * NUM * sizeof(float), C, 0, NULL, NULL);

    if (status != CL_SUCCESS) {
        fprintf(stderr, "%s\n", "ewb obuf");
        exit(1);
    }

    unsigned char *binaddr = (unsigned char *)__binary_curry;

    // cl_program program = clCreateProgramWithSource(context, 1, (const char**) &kernel_cl, NULL, &status);
    cl_program program = clCreateProgramWithBinary(context, 1, devices, &__binary_curry_len, &binaddr, NULL, &status);

    if (status != CL_SUCCESS) {
        fprintf(stderr, "%s\n", "create program");
        exit(1);
    }

    status = clBuildProgram(program, numDevices, devices, NULL, NULL, NULL);

    // Create the kernel

    cl_kernel kernel;
    kernel = clCreateKernel(program, "conv", &status);


    // Associate the input and output buffers with the kernel 
    status = clSetKernelArg(kernel, 0, sizeof(cl_mem), &conv_layer_ibuf);
    status = clSetKernelArg(kernel, 1, sizeof(cl_mem), &conv_layer_wbuf);
    status = clSetKernelArg(kernel, 2, sizeof(cl_mem), &conv_layer_obuf);

    size_t globalWorkSize[1];

    globalWorkSize[0] = NUM;

    status = clEnqueueNDRangeKernel(cmdQueue, kernel, 1, NULL, 
    globalWorkSize, NULL, 0, NULL, NULL);


    clEnqueueReadBuffer(cmdQueue, conv_layer_obuf, CL_TRUE, 0, IMROW * IMROW * NUM * sizeof(float), C, 0, NULL, NULL);

    // ReLU
    for (int i = 0; i < NUM; i++) {
        for (int h = 0; h < IMROW; h++) {
            for (int w = 0; w < IMROW; w++) {
                C[i][h][w] = fmax(0, C[i][h][w]);
            }
        }
    }

    // Max pooling
    for (int i = 0; i < NUM; i++) {
        for (int h = 0; h < OUTIMROW; h++) {
            for (int w = 0; w < OUTIMROW; w++) {
                float local_max = C[i][2 * h][2 * w];
                local_max = fmax(local_max, C[i][2 * h + 1][2 * w]);
                local_max = fmax(local_max, C[i][2 * h + 1][2 * w + 1]);
                local_max = fmax(local_max, C[i][2 * h][2 * w + 1]);
                Cout[i][h][w] = local_max;
            }
        }
    }
}


